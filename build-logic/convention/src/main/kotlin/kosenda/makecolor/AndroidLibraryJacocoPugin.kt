@Suppress("unused")
class AndroidLibraryJacocoPlugin: Plugin<Project> {
    override fun apply(target: Project) {
        with(target) {
            with(pluginManager) {
                apply("org.gradle.jacoco")
                apply("com.android.library")
            }
            configureJacoco()
        }
    }
}